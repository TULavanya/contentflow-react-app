// Utility function to get feature image
export const getFeatureImage = (title: string, fallback?: string): string => {
  // Try direct mapping first
  if (FeatureMapping[title as keyof typeof FeatureMapping]) {
    return FeatureMapping[title as keyof typeof FeatureMapping];
  }
  
  // Try keyword matching
  const lowerTitle = title.toLowerCase();
  
  if (lowerTitle.includes('ai') || lowerTitle.includes('intelligent') || lowerTitle.includes('smart')) {
    return FeatureIcons.ai;
  }
  if (lowerTitle.includes('analytic') || lowerTitle.includes('data') || lowerTitle.includes('insight')) {
    return FeatureIcons.analytics;
  }
  if (lowerTitle.includes('security') || lowerTitle.includes('secure') || lowerTitle.includes('protection')) {
    return FeatureIcons.security;
  }
  if (lowerTitle.includes('content') || lowerTitle.includes('cms')) {
    return FeatureIcons.content;
  }
  if (lowerTitle.includes('develop') || lowerTitle.includes('code') || lowerTitle.includes('api')) {
    return FeatureIcons.development;
  }
  if (lowerTitle.includes('support') || lowerTitle.includes('help')) {
    return FeatureIcons.support;
  }
  if (lowerTitle.includes('training') || lowerTitle.includes('learn') || lowerTitle.includes('education')) {
    return FeatureIcons.training;
  }
  if (lowerTitle.includes('performance') || lowerTitle.includes('speed') || lowerTitle.includes('fast')) {
    return FeatureIcons.performance;
  }
  if (lowerTitle.includes('collaboration') || lowerTitle.includes('team') || lowerTitle.includes('together')) {
    return FeatureIcons.collaboration;
  }
  if (lowerTitle.includes('automation') || lowerTitle.includes('workflow')) {
    return FeatureIcons.automation;
  }
  
  // Default fallback
  return fallback || FeatureIcons.innovation;
};

